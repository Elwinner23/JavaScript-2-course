// ПУЗЫРЬКОВАЯ СОРТИРОВКА
function bubbleSort(arr) {
    const n = arr.length;
    // Проходим по всем элементам массива
    for (let i = 0; i < n; i++) {
    // Узнаём, были ли перестановки на текущей итерации
    let swapped = false;
    // Сравниваем элементы и делаем перестановки
    for (let j = 0; j < n - 1 - i; j++) {
    // Сравниваем пару соседних элементов
    if (arr[j] > arr[j + 1]) {
    // Если текущий элемент больше следующего, меняем их местами
    [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
    swapped = true; // Произошла хотя бы одна перестановка
    }
    }
    // Если на данной итерации не было перестановок, массив уже отсортирован
    if (!swapped) {
    break;
    }
    }
    
    return arr;
    }
    
    // Выводим результат в консоль
    const arr = [0, 23, 45, 17, 108, 67];
